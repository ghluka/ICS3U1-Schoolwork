age = int(input("What is your age? "))
year = int(input("What year is it? "))

add_1 = int(input("How many years to pass? "))
add_2 = int(input("How many years to pass again? "))

print(f"Today (in {year}) you are {age}, and in {year+add_1} you will be {age+add_1}, and in {year+add_1+add_2} you will be {age+add_1+add_2}")